The table is divided into two main categories:  
1. * *Custom(managed by the user) * * – meaning that these layers must be managed by the client.  
2. **As Service (managed by the provider)** – these layers are provided as a service by the cloud provider.  

### What does the user manage?  
- **IaaS:**The user manages everything above the operating system (OS), including containers, runtime, data, and applications.  
- **CaaS:**The user manages applications, data, and runtime, but containers are provided as a service.  
- **PaaS:**The user manages only applications and data, while the runtime and everything below it are provided as a service.  
- **BaaS:**The user manages only the data and sometimes partially the applications.  
- **SaaS:**The user simply uses ready-made applications without managing any infrastructure.  

### What does the provider manage?  
- In **IaaS**: Networking, storage, servers, and virtualization.  
- In **CaaS**: Also provides the operating system and containers.  
- In **PaaS**: Also includes the runtime environment.  
- In **BaaS**: Adds backend services like databases, authentication, etc.  
- In **SaaS**: Everything is managed by the provider, including the application itself.  

### Conclusion  
- **IaaS** is suitable for those who want the most control over infrastructure.  
- **CaaS** is specialized for working with containers.  
- **PaaS** makes development easier by eliminating the need to manage infrastructure.  
- **BaaS** is great for mobile and web apps that require backend services.  
- **SaaS** is for end users who simply use applications without technical maintenance.








