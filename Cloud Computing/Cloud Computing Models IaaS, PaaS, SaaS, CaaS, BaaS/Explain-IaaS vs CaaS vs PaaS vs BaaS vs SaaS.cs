Таблицата е разделена на две основни категории:  
1. * *Custom(управлявано от потребителя) * * – означава, че тези нива трябва да бъдат управлявани от клиента.  
2. **As Service (управлявано от доставчика)** – тези нива се предоставят като услуга от доставчика на облака.  

### Какво управлява потребителят?  
- **IaaS:**Потребителят управлява всичко над операционната система (OS), включително контейнери, среда за изпълнение (Runtime), данни и приложения.  
- **CaaS:**Потребителят управлява приложенията, данните и средата за изпълнение, но контейнерите вече се предоставят като услуга.  
- **PaaS:**Потребителят управлява само приложенията и данните, но средата за изпълнение и всичко под нея се предоставя като услуга.  
- **BaaS:**Потребителят управлява само данните и (понякога) частично приложенията.  
- **SaaS:**Потребителят използва готови приложения, без да се грижи за останалата инфраструктура.  

### Какво предоставя доставчикът?  
- При **IaaS**: мрежа, съхранение, сървъри и виртуализация.  
- При **CaaS**: добавени операционна система и контейнери.  
- При **PaaS**: добавена среда за изпълнение (Runtime).  
- При **BaaS**: добавени бекенд услуги като база данни, удостоверяване и др.  
- При **SaaS**: всичко е управлявано от доставчика, включително самото приложение.  

### Заключение  
- **IaaS** е подходящ за тези, които искат най-голям контрол над инфраструктурата.  
- **CaaS** е специализиран за работа с контейнери.  
- **PaaS** улеснява разработчиците, като премахва нуждата от управление на инфраструктурата.  
- **BaaS** е добър за мобилни и уеб приложения, които изискват бекенд услуги.  
- **SaaS** е за крайни потребители, които просто използват приложения без техническа поддръжка.