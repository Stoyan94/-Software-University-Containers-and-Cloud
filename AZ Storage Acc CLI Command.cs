 **Azure CLI** **Storage Account** в Azure. Нека разгледаме всеки елемент от нея:

### Команда:
```bash
az storage account create --name storageaccstoyan --resource-group StorageRG --location northeurope --sku Standard_LRS --kind StorageV2
```

### Разяснение на параметрите:

1. **az storage account create**:
   -Това е командата за създаване на нов **Storage Account** в Azure. `az` е командният интерфейс (CLI), 
    който използваме за взаимодействие с Azure, а `storage account create` указва, че искаме да създадем нов акаунт за съхранение.

2. **--name storageaccstoyan**:
   -Задава името на новия **Storage Account**. В този случай, акаунтът ще се казва `storageaccstoyan`. 
    Имената на Storage Accounts трябва да бъдат уникални в Azure, така че ако това име вече е заето, командата ще се провали.

3. **--resource-group StorageRG**:
   -Определя името на **Resource Group**, в който ще бъде създаден Storage Account. 
    В този пример, ресурсната група се нарича `StorageRG`. Ресурсната група е логическо обединение на всички ресурси в Azure, които се управляват заедно.

4. **--location northeurope**:
   -Указва * *локацията * *на ресурсите, които ще бъдат създадени. 
    В случая, локацията е `northeurope`, което означава, че акаунтът за съхранение ще бъде създаден 
    в Северна Европа (например Ирландия или някой от другите региони на Azure в Европа).

5. **--sku Standard_LRS**:
   -**SKU * *(Stock Keeping Unit) определя типа на съхранението. В случая `Standard_LRS` означава, 
    че Storage Account ще бъде с **стандартно съхранение** с **локално репликиране** (LRS - Locally Redundant Storage). 
    Това означава, че данните ще бъдат репликирани в рамките на един и същи регион за по-висока надеждност.

6. **--kind StorageV2**:
   -Определя типа на Storage Account. **StorageV2** е най-модерният тип акаунт, който поддържа всички функции на Azure Storage, 
    включително различни видове съхранение като Blob, File, Queue и Table.
    Това е универсален акаунт за съхранение и е подходящ за широк спектър от приложения.

### Какво прави тази команда?

Тази команда създава **Azure Storage Account** с име `storageaccstoyan` в ресурсната група `StorageRG` в региона `northeurope`.
Акаунтът ще използва **стандартно съхранение (Standard)** и **локално репликиране (LRS)**, като ще бъде от тип **StorageV2**.

Това е полезно, когато искате да съхранявате данни в Azure, например за **Blob Storage**, който може да бъде използван за съхранение на файлове, карти, резервни копия и други подобни.

След като акаунтът е създаден, може да създадеш **Blob Container** за съхранение на данни, включително Terraform state файла, както обясних по-рано.

### Защо използваме тази команда?

- Създава **Storage Account** за съхранение на данни.
- Подготвя място за съхранение на Terraform state файлове, ако искаш да използваш Azure за съхранение на състоянието.
- Използва **локално репликиране (LRS)** за висока надеждност в рамките на един регион.



```bash
az storage container create -n terraformcontainer --account-name storageaccstoyan
```
Командата създава **Blob Container** в **Azure Storage Account**.


### Параметри:
- **-n terraformcontainer**: Името на **Blob Container**.
- **--account-name storageaccstoyan**: Името на **Storage Account**, в който ще бъде създаден контейнерът.

### Какво прави:
- Създава контейнер с име `terraformcontainer` в **Storage Account** `storageaccstoyan`.
- Контейнерите се използват за съхранение на данни, като например **Terraform state файлове**.

### Защо е важно:
- Използва се за съхранение на **Terraform state файл** в **Azure**, което е необходимо за централизирано управление на инфраструктурата.
- Позволява да се организират и съхраняват различни данни.

Ако искате да използвате контейнера с Terraform, трябва да го настроите като **backend** в конфигурационния файл.