В Linux можеш да създаваш директории с командата mkdir. Ето няколко примера:

Основни команди:
Създаване на една директория
mkdir my_directory


Създаване на вложени директории наведнъж
mkdir -p parent/child/grandchild
Флагът -p създава всички необходими поддиректории, ако не съществуват.

Създаване на няколко директории наведнъж
mkdir dir1 dir2 dir3

Създаване на директории с последователни имена
mkdir dir_{1..5}
Това ще създаде dir_1, dir_2, dir_3, dir_4, dir_5.



Хакерии и трикове:
Създаване на директория с "невидимо" име

mkdir " "
Това създава директория с празно име (интервал), която може да е трудна за намиране. 
За да я влезеш в нея:
cd " "


Създаване на директория с escape символи
mkdir $'\n'

Можеш да я изтриеш така:
rm -rf $'\n'


Създаване на директория с табулация в името
mkdir $'\t'
Това ще направи директория с име "таб". 

Можеш да я изтриеш с:
rm -rf $'\t'


Създаване на директория със специални символи
mkdir -- "-l"
Това прави директория с име -l, която изглежда като флаг. 
За да я изтриеш:
rmdir -- "-l"


Създаване на директория в /dev/shm/ за временни хакерски файлове

В директорията /dev/shm/ (споделена памет) можеш да създадеш директория за временни файлове, които изчезват при рестарт:
mkdir /dev/shm/my_secret

Това се ползва от хакерите за съхраняване на злонамерени скриптове, защото не се записва в диска.

Създаване на "неизтриваема" директория
mkdir protected_folder
chmod -w protected_folder
Това премахва правото за писане и пречи на изтриването, освен ако не върнеш правата с:
chmod +w protected_folder


Използване на mktemp за временни директории
mktemp -d /tmp/secret_XXXX
Това създава директория с уникално име, напр. /tmp/secret_KJH8. Полезно за скриптове.