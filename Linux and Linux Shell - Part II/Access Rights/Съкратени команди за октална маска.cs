chown -R user:group directory — Рекурсивно променя собственика и групата на всички файлове и директории вътре в указаната директория.

chgrp -R group directory — Рекурсивно променя групата на всички файлове и директории вътре в указаната директория.


Заключение
Използването на тези команди в комбинация с опции като -R (рекурсивно) може да помогне при управлението на права и собственици на файлове и директории, особено когато работиш с големи количества файлове или директории.