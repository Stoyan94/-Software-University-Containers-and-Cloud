примери за командите chmod, chown, и chgrp, включително такива за директории:

1.chmod(Промяна на права за достъп)

Примери за файлове:
chmod u+x file — Добавя изпълнителни права на собственика на файла.

chmod g-w file — Премахва правото за писане от групата.

chmod o=r file — Настройва правото за четене за другите потребители.

chmod a+x file — Добавя изпълнителни права за всички (собственик, група, други).

chmod 644 file — Права за четене и писане за собственика, само за четене за групата и другите. (644 = 4 + 2 + 0 за собственика, 4 + 0 + 0 за групата и другите).

chmod 777 file — Дава пълни права на всички (чете, пише, изпълнява) за собственика, групата и другите.



Примери за директории:

chmod u+x directory — Добавя изпълнителни права на собственика за директорията. Това означава, че собственикът може да навигира в директорията (т.е., да влезе в нея).

chmod g+rx directory — Добавя права за четене и изпълнение за групата, което означава, че групата може да чете съдържанието и да навигира в директорията.

chmod a+rx directory — Добавя права за четене и изпълнение за всички потребители (собственик, група, други).

chmod -R 755 directory — Рекурсивно задава права за четене, писане и изпълнение на собственика и права за четене и изпълнение за групата и другите за всички файлове и директории вътре в указаната директория.


2. chown (Промяна на собственик и група на файл или директория)

Примери за файлове:

chown user file — Променя собственика на файла на "user".

chown user:group file — Променя собственика на файла на "user" и групата на "group".

chown :group file — Променя групата на файла на "group", без да променя собственика.

chown user: file — Променя само собственика на файла на "user", без да променя групата.


Примери за директории:

chown user:group directory — Променя собственика и групата на директорията на "user" и "group".

chown -R user:group directory — Рекурсивно променя собственика и групата на всички файлове и подпапки в директорията.

chown -R user: directory — Рекурсивно променя собственика на всички файлове и подпапки в директорията на "user", без да променя групата.



3. chgrp (Промяна на група на файл или директория)

Примери за файлове:

chgrp group file — Променя групата на файла на "group".

chgrp -v group file — Променя групата на файла на "group" и показва информация за промените.

chgrp --reference=another_file file — Променя групата на файла, така че тя да съвпада с групата на another_file.


Примери за директории:

chgrp group directory — Променя групата на директорията на "group".

chgrp -R group directory — Рекурсивно променя групата на всички файлове и подпапки в директорията.


4. Полезни комбинации

Рекурсивно за директории и поддиректории:

