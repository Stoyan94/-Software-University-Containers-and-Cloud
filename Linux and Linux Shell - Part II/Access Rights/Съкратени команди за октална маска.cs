Това ще осигури на собственика на файла да може да го чете, променя и изпълнява, но не променя правата на групата или останалите потребители.

Ако искаш да зададеш други права на групата или останалите потребители, можеш да добавиш символите g (група) или o (останалите). 
Например:

chmod g+rx script.sh – Добавя права за четене и изпълнение за групата.
chmod o+x script.sh – Добавя само права за изпълнение за останалите потребители.
